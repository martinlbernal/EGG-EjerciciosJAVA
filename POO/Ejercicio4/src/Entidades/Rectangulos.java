package Entidades;

public class Rectangulos {
	private double base;
	private double altura;
	
	
	public Rectangulos() {
		
	}
	
	
	public Rectangulos(double base, double altura) {
		super();
		this.base = base;
		this.altura = altura;
	}


	
	public double getBase() {
		return base;
	}


	public void setBase(double base) {
		this.base = base;
	}


	public double getAltura() {
		return altura;
	}


	public void setAltura(double altura) {
		this.altura = altura;
	}
	
	

}
